#Rumus baris aritmatika : Sn = n/2 (2a+(n-1)b)
#sumber: https://rumus.co.id/deret-aritmatika/
#soal : deret aritmatika untuk menghitung jumlah suku ke-99 dari deret 3, 7, 11

#suku yang dicari
n = 99
#suku pertama
a = 3
#beda suku pertama ke kedua
b = 4
#rumus menghitung
S99 = n / 2 * ( 2 * a + ( n - 1 ) * b )

print("Jumlah suku ke-99 deret tersebut adalah", S99)
